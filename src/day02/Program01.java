
package day02;

public class Program01 {

	public static void main(String[] args) {

		cal(10, 20);

		System.out.println("##########################");

		cal(30, 50);
	}

	public static void cal(int a, int b) {
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);

	}

}
